package in.com.Set_Get;

public class Shape {
    String color;
    int width;
    int price;
    public void setColor(String color) {
    	this.color = color;
    }
    public String getColor() {
    	return color ;
    }
    public void setWidth(int width) {
    	this.width = width;
    }
    public int getWidth() {
    	return width; 
    }
    public void setPrice(int price) {
    	this.price = price;
    }
    public int getPrice() {
    	return price;
    }
}
