package Set_Get;

public class bottle {
  String color;
  int hight;
  int length;
  int price;
  public void setColor(String color) {
	  this.color = color;
  }  
  public String getColor() {
	 return color;
  }
  public void setHight(int hight) {
	  this.hight = hight;
  }
  public int getHight() {
	  return hight;
  }
  public void setLength(int length) {
	  this.length = length;
  }
  public int getLength() {
	  return length;
  }
  public void setPrice(int price) {
	  this.price = price;
  }
  public int getPrice() {
	  return price;
  }
}